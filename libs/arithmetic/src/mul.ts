/**
 * Multiplies two numbers.
 * @param x The first number.
 * @param y The second number.
 * @returns The product of the two numbers.
 * @example
 * ```typescript
 * // Return 1:
 * mul(1, 1);
 * ```
 * @example
 * ```typescript
 * // Return 3:
 * mul(3, 1);
 * ```
 * @example
 * ```typescript
 * // Return -3:
 * mul(1, -3);
 * ```
 */
export function mul(x: number, y: number): number {
  return x * y;
}
