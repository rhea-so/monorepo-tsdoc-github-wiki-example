/**
 * Divides two numbers.
 * @param x The first number.
 * @param y The second number.
 * @returns The quotient of the two numbers.
 * @example
 * ```typescript
 * // Return 1:
 * div(1, 1);
 * ```
 * @example
 * ```typescript
 * // Return 3:
 * div(3, 1);
 * ```
 * @example
 * ```typescript
 * // Return -0.3333333333333333:
 * div(1, -3);
 * ```
 */
export function div(x: number, y: number): number {
  return x / y;
}
